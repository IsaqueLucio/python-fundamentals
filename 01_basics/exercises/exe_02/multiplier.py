'''
Ask for a number using input(). Remember to convert it (cast) to float. 
Multiply that number by 3.5 and print the result with an f-string.
'''

# 1. Asking for the number that will be used
num = float(input("Input the number that will be multplied by 3,5: "))

# 2. Showing the result of the multiplication
print(f"Result: ",num," x 3,5: ",num*3.5)