'''
Define two variables with the values 15 and 4.
Print the result of normal division (/), integer division (//), and the remainder of the division (%).
'''

# 1. Creating the variables
a = 15
b = 4

# 2. Defining the operations and showing they
print(f"Normal division: {a} ÷ {b} = {a/b}")
print(f"Integer division: {a} ÷ {b} = {a//b}")
print(f"Modulo: {a} % {b} = {a%b}")